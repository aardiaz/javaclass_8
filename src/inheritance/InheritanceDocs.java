package inheritance;

public class InheritanceDocs {
	
	/*
	 * -------------- Inheritance --------------------
	 *  # use for code re-usability
	 *  # process of accessing properties and methods of parent/super/base class
	 *    to child/sub/derived class is known as inheritance.
	 *    
	 *  # Also called IS-A relationship.
	 *  # Its a Generalization to Specialization process.
	 *  # we can inherit only one class at a time.
	 *  # we can't inherit private properties,methods and constructors.
	 *  # we can't inherit final class.
	 *  # we can inherit static things.
	 *  # 'extends' keyword is used in inheritance.
	 *  # Also known as value change of class.
	 *  
	 *    #syntax :
	 *    
	 *      child-class   extends  parent-class{
	 *      
	 *            //properties: child + parent
	 *            
	 *            //methods : child + parent
	 *      }
	 *      
	 *      e.g.
	 *      
	 *      class A{
	 *      
	 *      }
	 *      
	 *      class B extends A{
	 *      
	 *      }
	 *      
	 *      class C extends B{
	 *      
	 *      }
	 */

}
