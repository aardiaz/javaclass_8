package abstraction;

public class AbstractionDocs {
	/*
	 * ----------------- Abstraction -----------------------------
	 *  # process of hiding implementation details in the program.
	 *  # To achieve abstraction :
	 *  
	 *   a> Abstract class :
	 *   	# class which contains abstract(unimplemented) methods and non-abstract method.
	 *          #Abstract method :
	 *             -> method which does not have body/implementation.
	 *             e.g.
	 *                 abstract void print();
	 *                 abstract int getArea(int l, int b);
	 *   
	 *       # every abstract class must have at least one child class and child
	 *         class must override abstract methods.
	 *         
	 *       # we cant create object of abstract class.
	 *       # level of abstraction 0-100%
	 *       
	 *            #syntax :
	 *            
	 *              abstract class   class_name{
	 *              
	 *                 //abstract methods
	 *                   abstract void sms();
	 *                 
	 *                 //non-abstract methods
	 *                 void add(){
	 *                 kdfkdsfksf
	 *                 sdfdsjflsdfks
	 *                 sdfdjdslfjlsfksldf
	 *                 }
	 *              
	 *              }
	 *   
	 *   b> Interface :
	 *   	# same as class which contains public abstract methods and public 
	 *        static final variables.
	 *        
	 *      # every interface have at least one implementation class and impl. class
	 *        must override abstract methods.
	 *        
	 *      # we can't create object of interface.
	 *      # level of abstraction 100%
	 *      # multiple inheritance exist in interface.
	 *      
	 *      # syntax :
	 *      
	 *            interface  interface-name{
	 *               
	 *                   //public abstract methods
	 *            }
	 *      
	 */

}
